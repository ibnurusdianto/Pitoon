"""
Data Structure
"""

# list - tipe data mutable yang bisa anda tambahkan, ubah, edit, dll 
list_nama = ["ibnu", "budi", "joko", "siti", "ani"]
print(f"list : ", list_nama)

# tuple - tipe data imutable yang tidak bisa diubah lagi 
list_nama = ("ibnu", "budi", "joko", "siti", "ani")
print(f"tuple : ", list_nama)

#  set - tipe data mutable namun unik, tidak boleh ada duplikasi 
list_nama = {"ibnu", "budi", "joko", "siti", "ani"}
print(f"set : ", list_nama)

# dict - tipe data mutable yang menyimpan data dalam bentuk key-value
list_nama = {"nama": "ibnu", "umur": 20, "pekerjaan": "programmer"}
print(f"dict : ", list_nama)   


