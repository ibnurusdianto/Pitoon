"""
Operator logika 

and akan bernilai true, jika keduanya true
or akan bernilai true, jika salah satu true atau keduanya true
not akan bernilai true, jika salah satu false, dan kebalikannya
"""

print(True and True) 
print(True and False)
print(False and True)
print(False and False)

print(True or True)
print(True or False)
print(False or True)
print(False or False)

print(not True)
print(not False)