import math 

# pangkat 
print(math.pow(2, 3)) # jika ingin sederhana gunakan module ini, untuk perpangkatan 

# mencari nilai factorial 
print(math.factorial(5))

# mencari nilai akar kuadrat
print(math.sqrt(16)) 

# ceil untuk mengembalikan nilai integer namun akan di bulatkan ke atas 
print(math.ceil(16.1)) # akan mengembalikan 17

# floor untuk mengembalikan nilai integer namun akan di bulatkan ke bawah 
print(math.floor(16.9)) # akan mengembalikan 16

# mengembalikan nilai absolut 
print(math.fabs(16.9)) # akan mengembalikan 16



